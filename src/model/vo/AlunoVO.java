package model.vo;

public class AlunoVO {
	
	private int id;
	private String nome;
	private String sobrenome;
	private String matricula;
	private double p1;
	private double p2;
	private double t1;
	private double t2;
	private double pesoProvas;
	private double pesoTrabalhos;
	private String nivel;
	private String situacao;
	private double notaFinal;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSobrenome() {
		return sobrenome;
	}
	public void setSobrenome(String sobrenome) {
		this.sobrenome = sobrenome;
	}
	public String getMatricula() {
		return matricula;
	}
	public void setMatricula(String matricula) {
		this.matricula = matricula;
	}
	public double getP1() {
		return p1;
	}
	public void setP1(double p1) {
		this.p1 = p1;
	}
	public double getP2() {
		return p2;
	}
	public void setP2(double p2) {
		this.p2 = p2;
	}
	public double getT1() {
		return t1;
	}
	public void setT1(double t1) {
		this.t1 = t1;
	}
	public double getT2() {
		return t2;
	}
	public void setT2(double t2) {
		this.t2 = t2;
	}
	public double getPesoProvas() {
		return pesoProvas;
	}
	public void setPesoProvas(double pesoProvas) {
		this.pesoProvas = pesoProvas;
	}
	public double getPesoTrabalhos() {
		return pesoTrabalhos;
	}
	public void setPesoTrabalhos(double pesoTrabalhos) {
		this.pesoTrabalhos = pesoTrabalhos;
	}
	public String getNivel() {
		return nivel;
	}
	public void setNivel(String nivel) {
		this.nivel = nivel;
	}
	public String getSituacao() {
		return situacao;
	}
	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}
	public double getNotaFinal() {
		return notaFinal;
	}
	public void setNotaFinal(double notaFinal) {
		this.notaFinal = notaFinal;
	}
	
	
}
